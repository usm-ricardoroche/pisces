#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum SummaryStyle {
	Function,
	Variable,
	Imports,
	Default,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct ChunkTraits {
	pub groupable:                bool,
	pub packed:                   bool,
	pub addressable_leaf:         bool,
	pub always_preserve_children: bool,
	pub has_addressable_members:  bool,
	pub summary:                  SummaryStyle,
	pub container:                bool,
}

const DEFAULT_TRAITS: ChunkTraits = ChunkTraits {
	groupable:                false,
	packed:                   false,
	addressable_leaf:         false,
	always_preserve_children: false,
	has_addressable_members:  false,
	summary:                  SummaryStyle::Default,
	container:                false,
};

const GROUP_TRAITS: ChunkTraits = ChunkTraits { groupable: true, ..DEFAULT_TRAITS };

const CONTAINER_TRAITS: ChunkTraits = ChunkTraits { container: true, ..DEFAULT_TRAITS };

const ADDRESSABLE_CONTAINER_TRAITS: ChunkTraits =
	ChunkTraits { container: true, has_addressable_members: true, ..DEFAULT_TRAITS };

const PRESERVE_CHILDREN_TRAITS: ChunkTraits =
	ChunkTraits { container: true, always_preserve_children: true, ..DEFAULT_TRAITS };

const PACKED_LEAF_TRAITS: ChunkTraits =
	ChunkTraits { packed: true, addressable_leaf: true, ..DEFAULT_TRAITS };

const FUNCTION_TRAITS: ChunkTraits =
	ChunkTraits { summary: SummaryStyle::Function, ..DEFAULT_TRAITS };

const VARIABLE_TRAITS: ChunkTraits =
	ChunkTraits { summary: SummaryStyle::Variable, ..DEFAULT_TRAITS };

const IMPORTS_TRAITS: ChunkTraits =
	ChunkTraits { groupable: true, summary: SummaryStyle::Imports, ..DEFAULT_TRAITS };

